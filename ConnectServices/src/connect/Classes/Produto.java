package connect.Classes;

public class Produto {
	
	private String codProduto;
	private String mercadoria;
	private String unid;
	private String codFornecedor;
	private Double valorCompra;
	private Double valorAvista;
	
	
	public String getCodProduto() {
		return codProduto;
	}
	public void setCodProduto(String codProduto) {
		this.codProduto = codProduto;
	}
	public String getMercadoria() {
		return mercadoria;
	}
	public void setMercadoria(String mercadoria) {
		this.mercadoria = mercadoria;
	}
	public String getUnid() {
		return unid;
	}
	public void setUnid(String unid) {
		this.unid = unid;
	}
	public String getCodFornecedor() {
		return codFornecedor;
	}
	public void setCodFornecedor(String codFornecedor) {
		this.codFornecedor = codFornecedor;
	}
	public Double getValorCompra() {
		return valorCompra;
	}
	public void setValorCompra(Double valorCompra) {
		this.valorCompra = valorCompra;
	}
	public Double getValorAvista() {
		return valorAvista;
	}
	public void setValorAvista(Double valorAvista) {
		this.valorAvista = valorAvista;
	}

	
	

}
